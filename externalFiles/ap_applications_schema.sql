PROCEDURE CREATE_SCENARIO_FROM_SEL_IDE (INJSON CLOB)
AS
SCENARIO_NAME   T_AT_SCENARIO.NAME%TYPE;
SCENARIO_ID     T_AT_SCENARIO.ID%TYPE;
STEP_ID         T_AT_STEPS.ID%TYPE;
METHOD_ID       T_AT_METHODS.ID%TYPE;
STEPS           JSON_ARRAY_T;
TARGETS         JSON_ARRAY_T;
BEGIN


SCENARIO_NAME := JSON_VALUE(INJSON, '$.name');
STEPS := JSON_ARRAY_T(JSON_QUERY(INJSON, '$.tests.commands'));

PK_AUTO_TESTING.CREATE_SCENARIO ('APP0000001', SCENARIO_NAME, 'Automated testing ' || SCENARIO_NAME, SCENARIO_ID);


FOR INDX IN 0 .. STEPS.GET_SIZE - 1
   LOOP
      
      IF(LOWER(JSON_VALUE(STEPS.GET(INDX).TO_STRING(), '$.command')) IN ('open'))
      THEN
      
        PK_AUTO_TESTING.CREATE_STEP (SCENARIO_ID, NULL, NULL, NULL, STEP_ID);
        PK_AUTO_TESTING.ADD_METHOD (STEP_ID, JSON_VALUE(STEPS.GET(INDX).TO_STRING(), '$.command'), METHOD_ID, JSON_VALUE(INJSON, '$.url'));
        
      ELSIF(LOWER(JSON_VALUE(STEPS.GET(INDX).TO_STRING(), '$.command')) IN ('click', 'type'))
      THEN
      
