function compareVersions(firstVersion varchar2, secondVersion varchar2) return number
as
    type versionT is record (major number, minor number, patch number);
    ver1    versionT;
    ver2    versionT;

begin

    ver1.major := REGEXP_SUBSTR(firstVersion, '[0-9]+',1,1);
    ver1.minor := REGEXP_SUBSTR(firstVersion, '[0-9]+',1,2);
    ver1.patch := REGEXP_SUBSTR(firstVersion, '[0-9]+',1,3);

    ver2.major := REGEXP_SUBSTR(secondVersion, '[0-9]+',1,1);
    ver2.minor := REGEXP_SUBSTR(secondVersion, '[0-9]+',1,2);
    ver2.patch := REGEXP_SUBSTR(secondVersion, '[0-9]+',1,3);

    if (ver1.major > ver2.major) then
        return 1;
    ELSIF (ver1.major < ver2.major) then
        return -1;
    else
        if (ver1.minor > ver2.minor) then
            return 1;
        ELSIF (ver1.minor < ver2.minor) then
            return -1;
        else
            if (ver1.patch > ver2.patch) then
                return 1;
            ELSIF (ver1.patch < ver2.patch) then
                return -1;
            else
                return 0;
            end if;
        end if;
    end if;
end;

