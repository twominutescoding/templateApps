  PROCEDURE GET_CONFIG(IN_USER IN VARCHAR2, IN_APPLICATION IN VARCHAR2, OUT_MEMORY OUT CLOB) AS
  ID_VALUE d_entities.ID%TYPE ;
  BEGIN
    SELECT ID INTO ID_VALUE FROM AP_APPLICATIONS.d_entities WHERE NAME = IN_APPLICATION;
    select json_arrayagg(json_object( 'varijabla: ' value MODULE, 
                                  'value: ' value  VALUE)returning clob ) into OUT_MEMORY
    from AP_APPLICATIONS.d_entity_attributes 
    where name = IN_USER
    and ENTITY = ID_VALUE;
  END GET_CONFIG;

  PROCEDURE SET_CONFIG(IN_USER IN VARCHAR2, IN_KEY IN VARCHAR2, IN_APPLICATION IN VARCHAR2, IN_VALUE IN CLOB, IN_CREATE_USER IN VARCHAR2, OUT_VALUE out VARCHAR2)AS
  ID_VALUE d_entities.ID%TYPE ;
  BEGIN
    ID_VALUE := NULL;
    SELECT ID INTO ID_VALUE FROM AP_APPLICATIONS.d_entities WHERE NAME = IN_APPLICATION;
    if (ID_VALUE is not null) then
        MERGE INTO d_entity_attributes d
            USING (select
            ID_VALUE ENTITY,
            IN_USER name,
            IN_KEY as module,
            'TABLE_CONFIGURATION' as purpose , 
            IN_VALUE as value,
            IN_CREATE_USER as CREATE_USER,
            sysdate as CREATE_DATE 
            from dual) s
        ON (d.name = s.name AND D.ENTITY = S.ENTITY AND D.module = S.module)
        WHEN MATCHED THEN 
            UPDATE SET d.value = s.value
        WHEN NOT MATCHED THEN 
            INSERT (ENTITY, name, module, purpose, value, CREATE_USER, CREATE_DATE) 
            VALUES (S.ENTITY, S.name, s.module, s.purpose, s.value, s.CREATE_USER, s.CREATE_DATE); 
        commit;
        OUT_VALUE := 'Uspjesno postavljene nove vrijednosti';
    else
        OUT_VALUE := 'Tag ' || IN_APPLICATION || ' ne postoji.';
    end if;
    EXCEPTION WHEN OTHERS THEN
        OUT_VALUE := 'Doslo je do greske: ' || SQLCODE || ': ' || SUBSTR(SQLERRM, 1, 200); 
  END SET_CONFIG;

END PK_TABLE_CONFIG;

